
package Encapsulacion;




public class Usuario {
    
    
   private String nombre;
   private int edad;

   
   //public es un motador en la encapsulacion
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
    
}
